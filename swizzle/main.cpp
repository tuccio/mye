template <typename T, int N>
void PrintVector(const Matrix<T, N, 1> &m)
{

	std::cout << "(";

	for (int i = 0; i < N; i++)
	{
		std::string sep = (i == N - 1 ? "" : ", ");
		std::cout << m[i] << sep;
	}

	std::cout << ")" << std::endl;

}

int main(int argc, char *argv[])
{

	Vector4 v(0.0f, 1.0f, 0.2f, 0.3f);

	PrintVector(v.xxxx());
	PrintVector(v.wxyz());
	PrintVector(v.xuvy());

	system("pause");

	return 0;

}

